var message;
// created a variable
message = "Hello, world.";
// assignment operator is the "=" sign
// "Hello, world." is the data stored
// surround text with single or double quotes, this is a string

console.log(message);
// log the variable to the console / passed a variable

message = "Everything changes."
// variables can change

console.log(message);
message = 101;
// numbers are the other data type

console.log(message);
// any variable can hold any data type

message = true;
// true and false are boolean, quotations not required
console.log(message);

